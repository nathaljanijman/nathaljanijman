// Language management
class LanguageManager {
    constructor() {
        this.currentLanguage = this.detectLanguage();
        this.updateURL();
        this.init();
    }

    detectLanguage() {
        // First check URL path for language (HIGHEST PRIORITY)
        const path = window.location.pathname;
        if (path.startsWith('/en') || path === '/en') {
            // Update localStorage to match URL
            localStorage.setItem('preferredLanguage', 'en');
            return 'en';
        }
        if (path.startsWith('/nl') || path === '/nl') {
            // Update localStorage to match URL
            localStorage.setItem('preferredLanguage', 'nl');
            return 'nl';
        }

        // Check URL query parameter
        const urlParams = new URLSearchParams(window.location.search);
        const langParam = urlParams.get('lang');
        if (langParam && (langParam === 'nl' || langParam === 'en')) {
            localStorage.setItem('preferredLanguage', langParam);
            return langParam;
        }

        // Check if language is stored in localStorage
        const stored = localStorage.getItem('preferredLanguage');
        if (stored && (stored === 'nl' || stored === 'en')) {
            return stored;
        }

        // Detect browser language
        const browserLang = navigator.language || navigator.userLanguage;
        const langCode = browserLang.toLowerCase();

        // If Dutch, use NL, otherwise default to NL (Dutch is primary language)
        if (langCode.startsWith('nl')) {
            localStorage.setItem('preferredLanguage', 'nl');
            return 'nl';
        }

        // Default to Dutch for primary audience
        localStorage.setItem('preferredLanguage', 'nl');
        return 'nl';
    }

    updateURL() {
        // Update browser URL to show language prefix without page reload
        const currentPath = window.location.pathname;

        // Only update URL if it doesn't already have a language prefix
        if (!currentPath.startsWith('/nl') && !currentPath.startsWith('/en')) {
            const isPortfolioPage = currentPath.includes('portfolio-website');
            let newPath;

            if (isPortfolioPage) {
                newPath = `/${this.currentLanguage}/portfolio-website`;
            } else if (currentPath === '/' || currentPath === '/index.html' || currentPath === '') {
                newPath = `/${this.currentLanguage}`;
            } else {
                // For other pages, add language prefix
                newPath = `/${this.currentLanguage}${currentPath}`;
            }

            // Use replaceState to update URL without triggering navigation
            if (window.history && window.history.replaceState) {
                window.history.replaceState({}, '', newPath + window.location.search + window.location.hash);
            }
        }
    }

    init() {
        this.applyTranslations();
        this.updateLanguageToggle();
        this.updateDynamicLinks();
        this.attachEventListeners();
    }

    attachEventListeners() {
        // Header language toggle (desktop)
        const languageToggle = document.getElementById('languageToggle');
        const languageDropdown = document.getElementById('languageDropdown');

        if (languageToggle && languageDropdown) {
            // Toggle dropdown on click
            languageToggle.addEventListener('click', (e) => {
                e.stopPropagation();
                languageDropdown.classList.toggle('active');
            });

            // Close dropdown when clicking outside
            document.addEventListener('click', () => {
                languageDropdown.classList.remove('active');
            });

            // Language options in dropdown
            const langOptions = languageDropdown.querySelectorAll('.lang-option');
            langOptions.forEach(option => {
                option.addEventListener('click', (e) => {
                    e.stopPropagation();
                    const lang = option.dataset.lang;
                    this.switchLanguage(lang);
                    languageDropdown.classList.remove('active');
                });
            });
        }

        // Footer language buttons
        const footerLangButtons = document.querySelectorAll('.footer-lang-btn');
        footerLangButtons.forEach(btn => {
            btn.addEventListener('click', () => {
                const lang = btn.dataset.lang;
                this.switchLanguage(lang);
            });
        });
    }

    switchLanguage(lang) {
        if (lang !== 'nl' && lang !== 'en') return;

        this.currentLanguage = lang;
        localStorage.setItem('preferredLanguage', lang);

        // Update HTML lang attribute
        document.documentElement.setAttribute('lang', lang);

        // Navigate to the correct language URL
        const currentPath = window.location.pathname;
        let newPath;

        // Determine the base page (without language prefix)
        let basePage = currentPath;
        if (currentPath.startsWith('/nl') || currentPath.startsWith('/en')) {
            basePage = currentPath.substring(3) || '/';
        }

        // Handle root path
        if (basePage === '/' || basePage === '') {
            newPath = `/${lang}`;
        } else {
            newPath = `/${lang}${basePage}`;
        }

        // Only navigate if the URL is different
        const newUrl = newPath + window.location.search + window.location.hash;
        if (window.location.pathname !== newPath) {
            window.location.href = newUrl;
        } else {
            // If URL is the same, just update the UI
            this.applyTranslations();
            this.updateLanguageToggle();
            this.updateFooterButtons();
            this.updateDynamicLinks();
        }
    }

    updateFooterButtons() {
        const footerButtons = document.querySelectorAll('.footer-lang-btn');
        footerButtons.forEach(btn => {
            if (btn.dataset.lang === this.currentLanguage) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }
        });
    }

    updateLanguageToggle() {
        const currentLangDisplay = document.querySelector('.lang-current');
        if (currentLangDisplay) {
            currentLangDisplay.textContent = this.currentLanguage.toUpperCase();
        }

        // Update footer buttons
        this.updateFooterButtons();
    }

    // Safe HTML sanitization for translations (whitelist approach)
    sanitizeTranslation(html) {
        // Only allow safe formatting tags
        const allowedTags = ['strong', 'em', 'b', 'i', 'br'];
        const div = document.createElement('div');
        div.textContent = html; // First escape everything

        // Then selectively allow safe tags
        let safe = div.innerHTML;
        allowedTags.forEach(tag => {
            const openRegex = new RegExp(`&lt;${tag}&gt;`, 'gi');
            const closeRegex = new RegExp(`&lt;/${tag}&gt;`, 'gi');
            safe = safe.replace(openRegex, `<${tag}>`);
            safe = safe.replace(closeRegex, `</${tag}>`);
        });

        return safe;
    }

    applyTranslations() {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.dataset.translate;
            if (translations[this.currentLanguage] && translations[this.currentLanguage][key]) {
                const translation = translations[this.currentLanguage][key];

                // For input placeholders
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = translation;
                }
                // For select options
                else if (element.tagName === 'OPTION') {
                    element.textContent = translation;
                }
                // For elements that may contain HTML (like <strong>, <em>)
                else if (translation.includes('<')) {
                    element.innerHTML = this.sanitizeTranslation(translation);
                }
                // For regular text content
                else {
                    element.textContent = translation;
                }
            }
        });
    }

    updateDynamicLinks() {
        // Update portfolio sticker link
        const portfolioLink = document.getElementById('portfolioStickerLink');
        if (portfolioLink) {
            portfolioLink.href = `/${this.currentLanguage}/portfolio-website`;
        }

        // Update mobile nav links to home
        const navHome = document.getElementById('navHome');
        const navProjects = document.getElementById('navProjects');
        const navAbout = document.getElementById('navAbout');
        const navContact = document.getElementById('navContact');

        if (navHome) navHome.href = `/${this.currentLanguage}#home`;
        if (navProjects) navProjects.href = `/${this.currentLanguage}#projects`;
        if (navAbout) navAbout.href = `/${this.currentLanguage}#about`;
        if (navContact) navContact.href = `/${this.currentLanguage}#contact`;

        // Update back link on portfolio website page
        const backLink = document.querySelector('.back-link');
        if (backLink) {
            const currentHref = backLink.getAttribute('href');
            if (currentHref === '/' || currentHref === '#') {
                backLink.href = `/${this.currentLanguage}`;
                // Add click handler to navigate correctly
                backLink.addEventListener('click', (e) => {
                    e.preventDefault();
                    window.location.href = `/${this.currentLanguage}`;
                });
            }
        }
    }

    // Expose translations for script.js
    get translations() {
        return translations;
    }
}

